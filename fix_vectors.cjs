const fs = require('fs');

function applyVectors(filePath, vectorMap) {
  let src = fs.readFileSync(filePath, 'utf8');
  let modified = false;

  for (const [id, newVec] of Object.entries(vectorMap)) {
    const vecStr = newVec.map(v => v.toFixed(2)).join(', ');
    // Match vector: [...] for this id — use [\s\S] to pass through {} in anecdotes
    const re = new RegExp(`(\\'${id}\\'[\\s\\S]{0,500}?vector:\\s*\\[)[^\\]]+(\\])`, '');
    const m = src.match(re);
    if (m) {
      const newBlock = m[1] + vecStr + m[2];
      if (m[0] !== newBlock) {
        src = src.replace(re, newBlock);
        modified = true;
      }
    } else {
      console.log(`  WARNING: ${id} not matched`);
    }
  }

  if (modified) {
    fs.writeFileSync(filePath, src);
    console.log(`✓ Vectors updated: ${filePath.split('/').pop()}`);
  } else {
    console.log(`  No changes: ${filePath.split('/').pop()}`);
  }
}

