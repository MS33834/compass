const DOMAINS = ['east-statesman', 'east-scientist', 'west-philosopher', 'west-scientist'];

for (const domain of DOMAINS) {
  const filePath = join(srcDir, `items.${domain}.ts`);
  const content = readFileSync(filePath, 'utf-8');
  
  // Backup
  mkdirSync(backupDir, { recursive: true });
  writeFileSync(join(backupDir, `items.${domain}.ts.bak`), content);
  
  // Find all question blocks and their properties (indentation: 2 spaces)
  const qBlockRegex = /  \{\n([\s\S]*?)\n  \},/g;
  let match;
  const questions = [];
  while ((match = qBlockRegex.exec(content)) !== null) {
    const block = match[0];
    const idMatch = block.match(/id:\s*'([^']+)'/);
    const formatMatch = block.match(/format:\s*'(\w+)'/);
    const traitMatch = block.match(/primary:\s*\{\s*traitId:\s*(\d+)/);
    if (idMatch && formatMatch && traitMatch) {
      questions.push({
        id: idMatch[1],
        format: formatMatch[1],
        traitId: parseInt(traitMatch[1]),
        fullBlock: block,
        index: match.index,
        length: match[0].length,
      });
    }
  }
  
  console.log(`${domain}: ${questions.length} questions found`);
  
  // Group by trait
  const byTrait = {};
  for (const q of questions) {
    if (!byTrait[q.traitId]) byTrait[q.traitId] = { situational: [], imagery: [], ranking: [], aesthetic: [] };
    byTrait[q.traitId][q.format].push(q);
  }
  
  // Build replacements: for each trait, convert 2 situational → ranking & aesthetic
  const rankData = R[domain];
  const aestData = A[domain];
  const replacements = [];
  
  for (const [tStr, groups] of Object.entries(byTrait)) {
    const t = parseInt(tStr);
    const s = groups.situational;
    if (s.length < 2) continue;
    
    let idx = 0;
    
    // 1st situational → ranking
    if (rankData && rankData[t]) {
      replacements.push({
        index: s[idx].index,
        length: s[idx].length,
        block: buildQuestionBlock(domain === 'east-statesman' ? 'es' : domain === 'east-scientist' ? 'esci' : domain === 'west-philosopher' ? 'wp' : 'ws', t, 'ranking', rankData[t]),
      });
      idx++;
    }
    
    // 2nd situational → aesthetic
    if (aestData && aestData[t]) {
      replacements.push({
        index: s[idx].index,
        length: s[idx].length,
        block: buildQuestionBlock(domain === 'east-statesman' ? 'es' : domain === 'east-scientist' ? 'esci' : domain === 'west-philosopher' ? 'wp' : 'ws', t, 'aesthetic', aestData[t]),
      });
    }
  }
  
  console.log(`  → ${replacements.length} replacements`);
  
  // Apply replacements in reverse index order
  replacements.sort((a, b) => b.index - a.index);
  
  let newContent = content;
  for (const r of replacements) {
    newContent = newContent.substring(0, r.index) + r.block + newContent.substring(r.index + r.length);
  }
  
  writeFileSync(filePath, newContent);
  console.log(`  ✅ Updated`);
}

