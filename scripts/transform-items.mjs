function generateAestheticOptions(traitId) {
  const opts = [
    ['前者','前者更合心意',1.2,null],
    ['偏前者','略偏前者之境',0.6,[[6,0.2]]],
    ['兼爱之','两种意境皆有可取',0.0,[[6,0.3]]],
    ['偏后者','略偏后者之境',-0.6,[[6,-0.2]]],
    ['后者','后者更入君心',-1.2,null],
    ['皆不取','超乎二者之外',-1.4,[[1,0.2]]],
  ];
  return opts.map(o => {
    let sec = '';
    if (o[3]) {
      sec = ', secondary: [' + o[3].map(s => `{ traitId: ${s[0]}, delta: ${s[1]} }`).join(', ') + ']';
    }
    return `      { text: '${o[0]}', gloss: '${o[1]}', primary: { traitId: ${traitId}, delta: ${o[2]} }${sec} }`;
  }).join(',\n');
}

function buildQuestionBlock(prefix, traitId, format, data) {
  const id = format === 'ranking' 
    ? `${prefix}-r${String(traitId).padStart(2, '0')}`
    : `${prefix}-a${String(traitId).padStart(2, '0')}`;
  
  let optsStr;
  if (format === 'ranking') {
    optsStr = data.o.map(o => {
      let sec = '';
      if (o[3]) {
        sec = ', secondary: [' + o[3].map(s => `{ traitId: ${s[0]}, delta: ${s[1]} }`).join(', ') + ']';
      }
      return `      { text: '${o[0]}', gloss: '${o[1]}', primary: { traitId: ${traitId}, delta: ${o[2]} }${sec} }`;
    }).join(',\n');
  } else {
    optsStr = generateAestheticOptions(traitId);
  }
  
  return `  {
    id: '${id}',
    format: '${format}',
    prompt: '${data.p}',
    promptGloss: '${data.g}',
    options: [
${optsStr},
    ],
  },`;
}

